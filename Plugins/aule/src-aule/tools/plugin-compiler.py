def extract_spec_from_dir(plugin_dir: Path) -> tuple[dict, list[str]]:
    """
    Reconstruct a PluginSpec-compatible dict from an existing plugin directory.

    Reads:
      .claude-plugin/plugin.json  → identity fields, connectors
      README.md                   → purpose, constraints, memory scope hints
      hooks/hooks.json            → memory scope (SessionClose → persistent)
      skills/*/SKILL.md           → component entries (skill type)
      agents/*.md                 → component entries (agent type)
      commands/*.md               → component entries (command type)

    Returns (raw_dict, pre_validation_warnings).
    pre_validation_warnings are soft issues found during extraction (e.g. missing
    optional fields) — not Pydantic errors. The raw_dict is then fed to parse_spec()
    which does the real validation.
    """
    warnings: list[str] = []
    raw: dict = {}

    # ── plugin.json ───────────────────────────
    pj_path = plugin_dir / ".claude-plugin" / "plugin.json"
    if not pj_path.exists():
        warnings.append("MISSING: .claude-plugin/plugin.json — cannot extract identity fields")
    else:
        try:
            pj = json.loads(pj_path.read_text())
        except json.JSONDecodeError as e:
            warnings.append(f"INVALID JSON: .claude-plugin/plugin.json — {e}")
            pj = {}

        raw["plugin_name"]  = pj.get("name", "")
        raw["version"]      = pj.get("version", "0.1.0")
        raw["description"]  = pj.get("description", "")
        raw["sublabel"]     = pj.get("sublabel", "Plugin")
        raw["icon"]         = pj.get("icon", "🔌")
        raw["keywords"]     = pj.get("keywords", [])

        author_raw = pj.get("author", {})
        if isinstance(author_raw, dict):
            raw["author"] = {
                "name":  author_raw.get("name", "Olytic Solutions"),
                "email": author_raw.get("email", "support@olyticsolutions.com"),
            }
        else:
            raw["author"] = {"name": str(author_raw), "email": "support@olyticsolutions.com"}

        # Connectors — exclude olytic-gateway (auto-injected by validator)
        raw_connectors = []
        for c in pj.get("connectors", []):
            if isinstance(c, dict) and c.get("id") != "olytic-gateway":
                raw_connectors.append({
                    "id":       c.get("id", "unknown"),
                    "required": c.get("required", True),
                    "scopes":   c.get("scopes", []),
                })
        raw["connectors"] = raw_connectors

    # ── README.md ─────────────────────────────
    readme_path = plugin_dir / "README.md"
    readme_text = ""
    if not readme_path.exists():
        warnings.append("MISSING: README.md — cannot extract purpose or constraints")
    else:
        readme_text = readme_path.read_text(encoding="utf-8")

    # Extract plugin_purpose: first non-empty paragraph after the title
    purpose = ""
    in_purpose = False
    for line in readme_text.splitlines():
        stripped = line.strip()
        if stripped.startswith("# ") and not in_purpose:
            in_purpose = True
            continue
        if in_purpose:
            if stripped.startswith(">"):
                purpose = stripped.lstrip("> ").strip()
                break
            if stripped and not stripped.startswith("#"):
                purpose = stripped
                break
            if stripped.startswith("#"):
                break
    raw["plugin_purpose"] = purpose or raw.get("description", "")

    # Extract key_functions: look for ## Components or ## Key Functions section
    key_functions: list[str] = []
    in_section = False
    for line in readme_text.splitlines():
        stripped = line.strip()
        if re.match(r"^## (Components|Key Functions|Features)", stripped, re.I):
            in_section = True
            continue
        if in_section:
            if stripped.startswith("##"):
                break
            m = re.match(r"^[-*]\s+\*?\*?([^*\-—:]+)", stripped)
            if m:
                key_functions.append(m.group(1).strip())
    raw["key_functions"] = key_functions if key_functions else ["[TODO: describe key functions]"]

    # Extract constraints: look for ## Constraints or ## Boundaries section
    constraints: list[str] = []
    in_constraints = False
    for line in readme_text.splitlines():
        stripped = line.strip()
        if re.match(r"^## (Constraints|Boundaries|Out of Scope)", stripped, re.I):
            in_constraints = True
            continue
        if in_constraints:
            if stripped.startswith("##"):
                break
            m = re.match(r"^[-*]\s+(.+)", stripped)
            if m:
                constraints.append(m.group(1).strip())
    raw["constraints"] = constraints

    # Extract memory scope: check README text, then hooks.json
    memory_scope = "ephemeral"
    if re.search(r"memory.?scope.*persistent|persistent.*memory", readme_text, re.I):
        memory_scope = "persistent"
    elif re.search(r"memory.?scope.*retrieval|retrieval.*memory", readme_text, re.I):
        memory_scope = "retrieval"

    # ── hooks.json → confirm persistent if SessionClose present ──
    hooks_path = plugin_dir / "hooks" / "hooks.json"
    if hooks_path.exists():
        try:
            hooks_data = json.loads(hooks_path.read_text())
            if "SessionClose" in hooks_data:
                memory_scope = "persistent"
        except json.JSONDecodeError:
            warnings.append("INVALID JSON: hooks/hooks.json — could not read hooks")

    raw["memory_scope"] = memory_scope

    # memory_access: synthesize a minimal valid block if persistent
    if memory_scope == "persistent":
        access_ctrl = "private"
        if re.search(r"access.?control.*shared|shared.*access", readme_text, re.I):
            access_ctrl = "shared"
        elif re.search(r"access.?control.*org.wide|org.wide.*access", readme_text, re.I):
            access_ctrl = "org-wide"
        raw["memory_access"] = {
            "access_control":     access_ctrl,
            "authorized_readers": [],
            "retention_days":     90,
            "purge_on_removal":   True,
        }

    # ── Components: skills ────────────────────
    components: list[dict] = []
    skills_dir = plugin_dir / "skills"
    plugin_name = raw.get("plugin_name", "")

    if skills_dir.exists():
        for skill_dir in sorted(skills_dir.iterdir()):
            if not skill_dir.is_dir():
                continue
            skill_name = skill_dir.name
            # Skip auto-generated skills — they're written by the compiler, not spec-declared
            if skill_name in (f"{plugin_name}-telemetry", f"{plugin_name}-session-summarizer"):
                continue
            skill_md = skill_dir / "SKILL.md"
            purpose_str, triggers = _parse_component_md(skill_md, warnings)
            components.append({
                "name":            skill_name,
                "type":            "skill",
                "purpose":         purpose_str,
                "trigger_phrases": triggers,
            })

    # ── Components: agents ────────────────────
    agents_dir = plugin_dir / "agents"
    if agents_dir.exists():
        for agent_md in sorted(agents_dir.glob("*.md")):
            agent_name = agent_md.stem
            purpose_str, triggers = _parse_component_md(agent_md, warnings)
            components.append({
                "name":            agent_name,
                "type":            "agent",
                "purpose":         purpose_str,
                "trigger_phrases": triggers,
            })

    # ── Components: commands ──────────────────
    commands_dir = plugin_dir / "commands"
    if commands_dir.exists():
        for cmd_md in sorted(commands_dir.glob("*.md")):
            cmd_name = cmd_md.stem
            purpose_str, triggers = _parse_component_md(cmd_md, warnings)
            components.append({
                "name":            cmd_name,
                "type":            "command",
                "purpose":         purpose_str,
                "trigger_phrases": triggers,
            })

    if not components:
        warnings.append("No user-declared components found (skills/agents/commands) — plugin has no domain logic")
    raw["components"] = components

    # ── Minimal defaults for optional fields ──
    raw.setdefault("telemetry_signals",        [])
    raw.setdefault("optimizer_feedback_points", [])
    raw.setdefault("success_metrics",          [])
    raw.setdefault("upstream_plugins",         [])
    raw.setdefault("downstream_plugins",       [])
    raw.setdefault("dimension",                None)

    return raw, warnings


def _parse_component_md(md_path: Path, warnings: list[str]) -> tuple[str, list[str]]:
    """
    Parse a skill/agent/command .md file.
    Returns (purpose_string, [trigger_phrases]).

    Extracts:
      - description from YAML frontmatter → purpose + triggers
      - Falls back to first H1 body paragraph if no frontmatter
    """
    if not md_path.exists():
        warnings.append(f"MISSING: {md_path}")
        return "[TODO: describe purpose]", ["[TODO trigger 1]", "[TODO trigger 2]",
                                            "[TODO trigger 3]", "[TODO trigger 4]"]

    text = md_path.read_text(encoding="utf-8")

    # Extract YAML frontmatter block
    fm_match = re.match(r"^---\s*\n(.*?)\n---", text, re.DOTALL)
    description_raw = ""
    if fm_match:
        fm_block = fm_match.group(1)
        # Handle block scalar (description: >) and inline description
        desc_block = re.search(r"^description:\s*>\s*\n((?:  .+\n?)+)", fm_block, re.MULTILINE)
        desc_inline = re.search(r"^description:\s*['\"]?(.+?)['\"]?\s*$", fm_block, re.MULTILINE)
        if desc_block:
            description_raw = textwrap.dedent(desc_block.group(1)).strip()
        elif desc_inline:
            description_raw = desc_inline.group(1).strip()

    if not description_raw:
        # Fallback: use first non-empty line after the first H1
        for line in text.splitlines():
            stripped = line.strip()
            if stripped and not stripped.startswith("#") and not stripped.startswith("---"):
                description_raw = stripped
                break

    # Split description into purpose (first sentence/line) and trigger phrases (remaining)
    # Convention: description starts with purpose, then "Use when the user says: ..."
    # or contains quoted phrases like "review this proposal"
    purpose = description_raw.split("\n")[0].strip() if description_raw else "[TODO: describe purpose]"

    # Extract quoted trigger phrases from description
    triggers = re.findall(r'"([^"]{4,80})"', description_raw)

    # Deduplicate while preserving order
    seen: set[str] = set()
    unique_triggers: list[str] = []
    for t in triggers:
        if t not in seen:
            seen.add(t)
            unique_triggers.append(t)

    # Pad to minimum 4 if short (Pydantic will catch this as an error, which is the intent)
    while len(unique_triggers) < 4:
        unique_triggers.append(f"[TODO trigger {len(unique_triggers) + 1}]")

    return purpose, unique_triggers


def validate_dir(plugin_dir: Path) -> None:
    """
    Entry point for --validate-dir mode.

    Extracts a PluginSpec-compatible dict from the directory, validates it through
    Pydantic, prints a structured report, and exits 0 (pass) or 1 (fail).
    """
    if not plugin_dir.exists():
        print(f"❌ Directory not found: {plugin_dir}")
        sys.exit(1)

    plugin_name = plugin_dir.name
    print(f"\n🔍 Validating plugin directory: {plugin_dir}\n")

    # ── Step 1: Extract ───────────────────────
    raw, extraction_warnings = extract_spec_from_dir(plugin_dir)

    if extraction_warnings:
        print("⚠️  Extraction warnings (file-level issues found before spec validation):\n")
        for w in extraction_warnings:
            print(f"  • {w}")
        print()

    # ── Step 2: Pydantic validation ───────────
    spec, errors = parse_spec(raw)

    if errors:
        print("❌ Spec validation failed — issues found in this plugin:\n")
        for e in errors:
            print(f"  • {e}")
        print()
        _print_validate_dir_summary(plugin_name, extraction_warnings, errors, passed=False)
        sys.exit(1)

    # ── Step 3: File-level structural checks ──
    # These mirror validate_output() but for an already-compiled directory
    structural_failures: list[str] = []
    name = raw.get("plugin_name", plugin_dir.name)

    required_files = [
        plugin_dir / ".claude-plugin" / "plugin.json",
        plugin_dir / "README.md",
        plugin_dir / "hooks" / "hooks.json",
        plugin_dir / "skills" / f"{name}-telemetry" / "SKILL.md",
    ]
    if raw.get("memory_scope") == "persistent":
        required_files.append(plugin_dir / "skills" / f"{name}-session-summarizer" / "SKILL.md")

    for path in required_files:
        if not path.exists():
            structural_failures.append(f"MISSING required file: {path.relative_to(plugin_dir)}")

    # Validate agent frontmatter: no <example> inside --- block
    agents_dir = plugin_dir / "agents"
    if agents_dir.exists():
        for agent_md in agents_dir.glob("*.md"):
            text = agent_md.read_text(encoding="utf-8")
            fm = re.match(r"^---\s*\n(.*?)\n---", text, re.DOTALL)
            if fm and "<example>" in fm.group(1):
                structural_failures.append(
                    f"YAML ERROR: {agent_md.name} has <example> tags inside frontmatter block — "
                    f"move them to after the closing ---"
                )
            # Check for unquoted colons in single-line description
            if fm:
                desc_m = re.search(r"^description:\s*(?!>)(.+)", fm.group(1), re.MULTILINE)
                if desc_m and ": " in desc_m.group(1):
                    structural_failures.append(
                        f"YAML ERROR: {agent_md.name} description contains ': ' on a single line — "
                        f"use 'description: >' block scalar format"
                    )

    # Validate skill frontmatter: name, description, version present
    skills_dir = plugin_dir / "skills"
    if skills_dir.exists():
        for skill_dir in skills_dir.iterdir():
            skill_md = skill_dir / "SKILL.md"
            if not skill_md.exists():
                structural_failures.append(f"MISSING: skills/{skill_dir.name}/SKILL.md")
                continue
            text = skill_md.read_text(encoding="utf-8")
            fm = re.match(r"^---\s*\n(.*?)\n---", text, re.DOTALL)
            if not fm:
                structural_failures.append(f"skills/{skill_dir.name}/SKILL.md: missing YAML frontmatter")
            else:
                for required_key in ("name:", "description:", "version:"):
                    if required_key not in fm.group(1):
                        structural_failures.append(
                            f"skills/{skill_dir.name}/SKILL.md: frontmatter missing '{required_key[:-1]}' field"
                        )

    if structural_failures:
        print("❌ Structural validation failed:\n")
        for f in structural_failures:
            print(f"  • {f}")
        print()
        _print_validate_dir_summary(plugin_name, extraction_warnings, structural_failures, passed=False)
        sys.exit(1)

    _print_validate_dir_summary(plugin_name, extraction_warnings, [], passed=True)
    print(f"\n✅ {plugin_name} passed spec validation — safe to package.\n")


def _print_validate_dir_summary(
    name: str,
    warnings: list[str],
    errors: list[str],
    passed: bool
) -> None:
    status = "✅ PASSED" if passed else "❌ FAILED"
    print(f"── Validation Summary: {name} ──")
    print(f"   Status:   {status}")
    print(f"   Warnings: {len(warnings)}")
    print(f"   Errors:   {len(errors)}")
    if not passed:
        print()
        print("   Aule: read the errors above and ask the user for the missing information.")
        print("   Fix the source files, then re-run --validate-dir.")


# ─────────────────────────────────────────────
# CLI ENTRY POINT
# ─────────────────────────────────────────────

def main() -> None:
    args = sys.argv[1:]

    # ── --validate-dir mode ───────────────────
    if "--validate-dir" in args:
        idx = args.index("--validate-dir")
        if idx + 1 >= len(args):
            print("Usage: python3 plugin-compiler.py --validate-dir <plugin-dir>")
            sys.exit(1)
        validate_dir(Path(args[idx + 1]))
        return

    # ── Normal compile / dry-run mode ─────────
    dry_run = "--dry-run" in args
    args = [a for a in args if a != "--dry-run"]

    if not args:
        print("Usage:")
        print("  python3 plugin-compiler.py [--dry-run] <spec.json>")
        print("  python3 plugin-compiler.py --validate-dir <plugin-dir>")
        sys.exit(1)

    spec_path = Path(args[0])
    if not spec_path.exists():
        print(f"❌ Spec file not found: {spec_path}")
        sys.exit(1)

    raw = json.loads(spec_path.read_text())
    spec, errors = parse_spec(raw)

    if errors:
        print("❌ Spec validation failed — Aule needs more information:\n")
        for e in errors:
            print(f"  • {e}")
        sys.exit(1)

    # Workspace root = 4 levels up from src-aule/tools/
    workspace_root = Path(__file__).parent.parent.parent.parent.parent

    if dry_run:
        print("🔍 DRY RUN — no files will be written\n")

    src = compile_plugin(spec, workspace_root, dry_run=dry_run)

    if not dry_run:
        failures = validate_output(src, spec)
        if failures:
            print("\n⚠️  Post-compile validation failures:")
            for f in failures:
                print(f"   • {f}")
            sys.exit(1)
        else:
            print("✅ Post-compile validation passed.")


if __name__ == "__main__":
    main()
