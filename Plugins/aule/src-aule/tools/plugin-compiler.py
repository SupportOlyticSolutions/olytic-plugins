def gen_plugin_json(spec: PluginSpec) -> str:
    manifest = {
        "name":        spec.plugin_name,
        "version":     spec.version,
        "description": spec.description,
        "author":      {"name": spec.author.name, "email": spec.author.email},
        "keywords":    spec.keywords,
        "hooks":       "hooks/hooks.json",
    }
    if spec.connectors:
        manifest["connectors"] = [
            {"id": c.id, "required": c.required, "scopes": c.scopes}
            for c in spec.connectors
        ]
    return json.dumps(manifest, indent=2, ensure_ascii=False)


def gen_readme(spec: PluginSpec) -> str:
    components_list = "\n".join(
        f"- **{c.name}** ({c.type.value}) — {c.purpose}"
        for c in spec.components
    )
    constraints_list = "\n".join(f"- {c}" for c in spec.constraints) or "- None declared"
    connectors_list = "\n".join(
        f"- `{c.id}` ({'required' if c.required else 'optional'})"
        for c in spec.connectors
    ) or "- None (standalone)"

    return f"""# {spec.plugin_name}

> {spec.description}

## Purpose

{spec.plugin_purpose}

## Components

{components_list}

## Constraints

{constraints_list}

## Integrations

{connectors_list}

## Memory

**Scope:** {spec.memory_scope.value}
{gen_readme_memory_section(spec)}

## Claude OS Identity

**Dimension:** {spec.dimension or "Not specified"}
**Upstream plugins:** {", ".join(spec.upstream_plugins) or "None"}
**Downstream plugins:** {", ".join(spec.downstream_plugins) or "None"}

---
*Generated by Aule Plugin Compiler v{COMPILER_VERSION} on {today_str()}*
"""


def gen_readme_memory_section(spec: PluginSpec) -> str:
    if spec.memory_scope == MemoryScope.ephemeral:
        return "Session-only. No data is retained between sessions."
    if spec.memory_scope == MemoryScope.persistent:
        ma = spec.memory_access
        ac = ma.access_control.value if ma else "private"
        readers = (", ".join(ma.authorized_readers) if ma and ma.authorized_readers else "N/A")
        return (
            f"Data is retained between sessions via vault.\n"
            f"**Access control:** {ac}\n"
            f"**Authorized readers:** {readers}\n"
            f"**Retention:** {ma.retention_days if ma else 90} days"
        )
    if spec.memory_scope == MemoryScope.retrieval:
        rc = spec.retrieval_config
        sources = (", ".join(rc.sources) if rc else "not configured")
        return f"RAG/retrieval mode. Sources: {sources}"
    return ""


