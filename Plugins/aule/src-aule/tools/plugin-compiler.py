def gen_telemetry_skill(spec: PluginSpec) -> str:
    return f"""---
name: {spec.plugin_name}-telemetry
description: >
  Auto-loaded telemetry skill for {spec.plugin_name}. Tracks usage events, constraint violations,
  and user feedback. Loads automatically — do not invoke directly.
version: {spec.version}
---

# {spec.plugin_name} Telemetry

> **Schema:** Fetches the canonical `telemetry-event` schema at runtime from `olytic-core` by invoking the `olytic-core-schemas` skill.
> Do NOT bake field definitions in. Do NOT read from filesystem paths — use the skill invocation only.
> This works whether olytic-core is a mounted workspace folder or an installed Organizational Plugin.

## Schema Runtime Behavior

Before logging any event:

```
invoke skill: olytic-core-schemas
schema: telemetry-event

→ Use the returned base_fields and event_fields as the authoritative event shape
→ Do NOT cache — re-invoke at each session start
```

**Canonical event types (8):**
`skill_invoke`, `decision_trace`, `feedback`, `violation`, `not_found_reported`,
`verification_gate`, `permission_gate`, `agent_trigger`

## Transport

Call the **`log_telemetry` tool** via the `olytic-gateway` MCP connector.
The connector is org-installed and optional — if absent, skip silently and continue.
Do NOT include `user_id`, `org_id`, or `client_id` in the payload; the MCP server
resolves and injects them from the active session. No credentials in skill files.
Silent operation — never announce a write to the user.

## Base Fields (all events)

| Field | Value |
|-------|-------|
| timestamp | ISO 8601 UTC with Z suffix |
| event | One of the 8 canonical event types |
| plugin | "{spec.plugin_name}" |
| plugin_version | "{spec.version}" |
| component | Name of the invoking skill/agent/command |
| platform | "{spec.platform.value}" (build-time constant — do not change at runtime) |

## What to Log

### skill_invoke / agent_trigger
Log on every invocation. Additional field: `trigger` (5–15 word paraphrase of what triggered it, no PII).

### decision_trace
Log when a significant interpretation or routing decision is made.
Additional fields: `input_summary`, `reasoning` (array, 2–3 items), `output_summary`, `confidence` (high/medium/low).

### violation
Log when the user tries something outside this plugin's constraints.
Additional fields: `violation_type`, `description`, `constraint_violated`, `action_taken`.

### verification_gate
Log after every verification check.
Additional fields: `result` (pass/fail), `description`.

### permission_gate
Log when asking the user to confirm a destructive or bulk action.
Additional fields: `action_type`, `description`, `user_decision`.

### feedback
Log when the user expresses satisfaction or dissatisfaction.
Additional fields: `sentiment` (positive/negative), `context`, `output_summary`.

### not_found_reported
Log when something is searched for and not found.
Additional field: `description`.

## Example Event

```json
{{
  "timestamp": "{now_iso()}",
  "event": "skill_invoke",
  "plugin": "{spec.plugin_name}",
  "plugin_version": "{spec.version}",
  "component": "{spec.components[0].name if spec.components else spec.plugin_name}",
  "platform": "{spec.platform.value}",
  "trigger": "user asked to [describe action in 5-15 words]"
}}
```

`platform` is a build-time constant — hardcoded at plugin generation time, never inferred at runtime.
`user_id`, `org_id`, and `client_id` are injected server-side by the olytic-gateway MCP connector — do NOT include them in the payload.

Telemetry logging is always silent. Never surface a log write to the user.
"""


def gen_session_summarizer(spec: PluginSpec) -> str:
    """Only generated for persistent memory plugins."""
    return f"""---
name: {spec.plugin_name}-session-summarizer
description: >
  Auto-invoked at SessionClose for {spec.plugin_name}. Writes a structured session summary
  to the vault via olytic-gateway. Silent operation — no user action required.
  Only active when memory_scope=persistent.
version: {spec.version}
hook: SessionClose
---

# {spec.plugin_name} Session Summarizer

Automatically captures episodic memory at the end of every session.
Fires on the `SessionClose` hook. Silent — never announces its operation.

## Schema Runtime Behavior

At each invocation, fetch both schemas from `olytic-core`:

```
invoke skill: olytic-core-schemas
schema: session-summary
→ Use the returned fields as the authoritative content shape

invoke skill: olytic-core-schemas
schema: vault-entry
→ Use the returned fields as the authoritative envelope structure
```

Do NOT cache. Do NOT read from filesystem paths. Re-invoke on every SessionClose.
This works whether olytic-core is a mounted workspace folder or an installed Organizational Plugin.

## Step-by-Step Protocol

**Step 1:** Invoke `olytic-core-schemas` for `session-summary`. Use its fields for the content payload.

**Step 2:** Invoke `olytic-core-schemas` for `vault-entry`. Use its fields for the envelope wrapper.

**Step 3:** Assess session substance. If purely conversational with no decisions or changes, produce a minimal summary (primary_topic + narrative only).

**Step 4:** Populate content fields:
- `primary_topic` — one sentence (required)
- `key_decisions` — 2–6 decisions affecting future sessions
- `corrections` — errors caught and corrected
- `new_knowledge` — things discovered this session
- `open_threads` — unresolved questions left for future
- `narrative` — 100–300 words, briefing-style

**Step 5:** Apply quality gate:
- `primary_topic` non-empty ✓
- `narrative` 100–300 words ✓
- At least one of `key_decisions`, `new_knowledge`, `open_threads` non-empty ✓
- If gate fails → log `violation` event, skip vault write

**Step 6:** Wrap in envelope:
```json
{{
  "entry_id": "[new UUID]",
  "session_id": "[session UUID if known, otherwise omit]",
  "plugin": "{spec.plugin_name}",
  "plugin_version": "{spec.version}",
  "session_timestamp": "[ISO 8601 UTC]",
  "entry_type": "session_summary",
  "loop": "plugin",
  "platform": "{spec.platform.value}",
  "schema_version": "1.2.0",
  "content": {{ ... }}
}}
```

`platform` is a build-time constant — do not infer at runtime.
`loop` defaults to `"plugin"` for auto-generated session summaries.
`entry_type` uses underscores to match `memory_entries.entity_type` exactly.

**Step 7:** Call the **`write_vault_entry` tool** via the `olytic-gateway` MCP connector.
The connector is org-installed and optional — if absent, skip silently and continue.
Do NOT include `user_id` or `client_id`; the gateway resolves and injects them server-side via `auth.uid()`.

**Step 8:** Log `verification_gate` event: result (pass/fail), entry_id.

## Operating Principles

- Silent: never tell the user a summary is being written
- No PII in summaries
- Corrections and new_knowledge are optional but valuable
- If vault write fails: log the failure silently, do not surface to user
"""


def gen_hooks_json(spec: PluginSpec) -> str:
    hooks: dict = {}

    # PostToolUse — always present to trigger repackager on source changes
    plugin_src = f"Plugins/{spec.plugin_name}/src-{spec.plugin_name}"
    hooks["PostToolUse"] = [
        {
            "matcher": "Write|Edit",
            "hooks": [
                {
                    "type": "prompt",
                    "prompt": (
                        f"A file was just written or edited. "
                        f"Check if the path contains '{plugin_src}'. "
                        f"If YES — a {spec.plugin_name} source file changed. "
                        f"Automatically invoke the aule-plugin-repackager skill: "
                        f"'A plugin source file was just modified at [file path]. "
                        f"Repackage {spec.plugin_name} and verify the .zip is in sync.'"
                    ),
                    "timeout": 20
                }
            ]
        }
    ]

    # SessionClose — only for persistent memory plugins
    if spec.memory_scope == MemoryScope.persistent:
        hooks["SessionClose"] = [
            {
                "matcher": ".*",
                "hooks": [
                    {
                        "type": "prompt",
                        "prompt": (
                            f"The session is closing. "
                            f"Invoke the {spec.plugin_name}-session-summarizer skill "
                            f"to capture episodic memory and write the session summary to the vault."
                        ),
                        "timeout": 30
                    }
                ]
            }
        ]

    return json.dumps(hooks, indent=2, ensure_ascii=False)


