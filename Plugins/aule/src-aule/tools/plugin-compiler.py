"""
Olytic Plugin Compiler
======================
Deterministic file generator. Takes a validated PluginSpec and writes a complete,
standards-compliant plugin to disk. No LLM involvement after this point.

Usage:
    python3 plugin-compiler.py <spec.json>              # compile from spec file
    python3 plugin-compiler.py --dry-run <spec.json>    # print file list without writing
    python3 plugin-compiler.py --validate-dir <dir>     # validate an existing plugin directory

Output:
    Plugins/<plugin-name>/src-<plugin-name>/...     # relative to workspace root

The compiler is invoked by Aule after the LLM translation layer produces a clean
PluginSpec. Aule calls parse_spec() first — if that succeeds, the compiler runs.
If parse_spec() fails, Aule surfaces the errors to the user and loops back.

--validate-dir mode:
    Reads an existing plugin directory, reconstructs a PluginSpec-compatible dict
    from the actual files, and validates it through Pydantic. Aule uses this after
    Update Mode edits — surgical fixes stay surgical, but the final result is still
    spec-verified. Exits 0 on pass, 1 on fail.

Version: 1.1.0
"""

