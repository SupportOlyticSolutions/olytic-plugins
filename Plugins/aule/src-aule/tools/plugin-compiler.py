def compile_plugin(spec: PluginSpec, workspace_root: Path, dry_run: bool = False) -> Path:
    """
    Write all plugin files to disk.

    Returns the path to the generated src directory.
    """
    name     = spec.plugin_name
    parent   = workspace_root / "Plugins" / name
    src      = parent / f"src-{name}"
    dot_cp   = src / ".claude-plugin"

    print(f"\n🔨 Compiling plugin: {name} v{spec.version}")
    print(f"   Output: {src}\n")

    # ── Manifest ──────────────────────────────
    write_file(dot_cp / "plugin.json", gen_plugin_json(spec), dry_run)

    # ── README ────────────────────────────────
    write_file(src / "README.md", gen_readme(spec), dry_run)

    # ── Hooks ─────────────────────────────────
    write_file(src / "hooks" / "hooks.json", gen_hooks_json(spec), dry_run)

    # ── Telemetry skill (always) ───────────────
    write_file(
        src / "skills" / f"{name}-telemetry" / "SKILL.md",
        gen_telemetry_skill(spec),
        dry_run
    )

    # ── Session summarizer (persistent only) ──
    if spec.memory_scope == MemoryScope.persistent:
        write_file(
            src / "skills" / f"{name}-session-summarizer" / "SKILL.md",
            gen_session_summarizer(spec),
            dry_run
        )

    # ── User-declared components ───────────────
    for component in spec.components:
        if component.type == ComponentType.skill:
            write_file(
                src / "skills" / component.name / "SKILL.md",
                gen_skill(spec, component),
                dry_run
            )
        elif component.type == ComponentType.agent:
            write_file(
                src / "agents" / f"{component.name}.md",
                gen_agent(spec, component),
                dry_run
            )
        elif component.type == ComponentType.command:
            write_file(
                src / "commands" / f"{component.name}.md",
                gen_command(spec, component),
                dry_run
            )

    # ── Metadata sidecar (parent level) ───────
    write_file(parent / f"{name}.metadata.json", gen_metadata(spec), dry_run)

    # ── Package (zip) ─────────────────────────
    if not dry_run:
        zip_path = parent / f"{name}.zip"
        with zipfile.ZipFile(zip_path, "w", zipfile.ZIP_DEFLATED) as zf:
            for file_path in src.rglob("*"):
                if file_path.is_file() and ".DS_Store" not in str(file_path):
                    zf.write(file_path, file_path.relative_to(src))
        print(f"  📦 {zip_path}")

    print(f"\n✅ {name} compiled successfully.")
    print(f"   Files written to: {src}")
    return src


