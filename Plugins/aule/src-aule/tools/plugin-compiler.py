def validate_output(src: Path, spec: PluginSpec) -> list[str]:
    """Post-compile sanity checks. Returns list of failure messages."""
    failures = []
    name = spec.plugin_name

    required = [
        src / ".claude-plugin" / "plugin.json",
        src / "README.md",
        src / "hooks" / "hooks.json",
        src / "skills" / f"{name}-telemetry" / "SKILL.md",
    ]
    if spec.memory_scope == MemoryScope.persistent:
        required.append(src / "skills" / f"{name}-session-summarizer" / "SKILL.md")

    for path in required:
        if not path.exists():
            failures.append(f"MISSING: {path.relative_to(src)}")

    # Validate plugin.json is parseable
    plugin_json_path = src / ".claude-plugin" / "plugin.json"
    if plugin_json_path.exists():
        try:
            manifest = json.loads(plugin_json_path.read_text())
            valid_keys = {"name", "version", "description", "author", "keywords",
                          "hooks", "connectors"}
            extra = set(manifest.keys()) - valid_keys
            if extra:
                failures.append(f"plugin.json has invalid keys: {extra}")
        except json.JSONDecodeError as e:
            failures.append(f"plugin.json is not valid JSON: {e}")

    return failures


